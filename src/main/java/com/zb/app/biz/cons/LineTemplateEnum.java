package com.zb.app.biz.cons;

/**
 * 0：线路；1：模板
 * 
 * @author Administrator 2014-8-21 下午2:47:03
 */
public enum LineTemplateEnum {

    Line(0, "线路"), Template(1, "模板");

    Integer value;
    String  name;

    private LineTemplateEnum(Integer value, String name) {
        this.value = value;
        this.name = name;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
